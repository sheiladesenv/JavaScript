// while (enquanto)
let x = 1;

while(x <= 10){
console.log(x);
x = x + 1;
}

let y = 15;
while(y >= 5){
    console.log(y);
    y--;
}

// Do While

let m = 15;

do{
    console.log(`${m}`);
    m = m + 2;
} while (m <= 21);

//for
for(i = 0; i <= 5; i++){
    console.log(i);
}
